<?php
require_once '../config/database.php';
session_start();

// Check if user is logged in
if (!isset($_SESSION['user_id'])) {
    header("Location: ../login.php");
    exit();
}

// Cek apakah tabel categories ada
try {
    $stmt = $pdo->query("SELECT * FROM categories ORDER BY name");
    $categories = $stmt->fetchAll(PDO::FETCH_ASSOC);
} catch (PDOException $e) {
    echo '<div style="margin:2rem;" class="alert alert-danger">Tabel <b>categories</b> belum ada di database. Silakan import ulang <b>database.sql</b> di phpMyAdmin.</div>';
    exit();
}

if ($_SERVER['REQUEST_METHOD'] == 'POST') {
    $title = $_POST['title'];
    $content = $_POST['content'];
    $status = $_POST['status'];
    $category_id = $_POST['category_id'] ?: null;
    $user_id = $_SESSION['user_id'];
    $slug = strtolower(str_replace(' ', '-', $title));
    
    // Handle image upload
    $image = null;
    if (isset($_FILES['image']) && $_FILES['image']['error'] == 0) {
        $allowed = ['jpg', 'jpeg', 'png', 'gif'];
        $filename = $_FILES['image']['name'];
        $ext = strtolower(pathinfo($filename, PATHINFO_EXTENSION));
        
        if (in_array($ext, $allowed)) {
            $new_filename = uniqid() . '.' . $ext;
            $upload_path = '../uploads/' . $new_filename;
            
            if (!is_dir('../uploads')) {
                mkdir('../uploads', 0777, true);
            }
            
            if (move_uploaded_file($_FILES['image']['tmp_name'], $upload_path)) {
                $image = $new_filename;
            }
        }
    }

    $stmt = $pdo->prepare("INSERT INTO articles (title, slug, content, image, status, category_id, user_id, created_at, updated_at) VALUES (?, ?, ?, ?, ?, ?, ?, NOW(), NOW())");
    
    if ($stmt->execute([$title, $slug, $content, $image, $status, $category_id, $user_id])) {
        header("Location: dashboard.php");
        exit();
    } else {
        $error = "Failed to create article. Please try again.";
    }
}
